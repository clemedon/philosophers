#!/bin/bash

makefile_rules()
{
    ARGS="1 410 200 200"

    echo "========================================="
    echo "===============[helgrind run]============"
    echo "========================================="
    make re helgrind_run ARGS="$ARGS";

    echo "========================================="
    echo "===============[valgrind run]============"
    echo "========================================="
    make re valgrind_run ARGS="$ARGS";

    echo "========================================="
    echo "===============[san thread]=============="
    echo "========================================="
    make fclean san_thread run ARGS="$ARGS";

    echo "========================================="
    echo "===============[san addr]================"
    echo "========================================="
    make fclean san_addr run ARGS="$ARGS";

    echo "========================================="
    echo "===============[make run]================"
    echo "========================================="
    make re run ARGS="$ARGS";
}

makefile_rules;
